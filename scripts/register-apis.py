    def step_2_google_cloud(self):
        """Google Cloud Vision registration (RECOMMENDED)"""
        self.print_header("Step 2: Google Cloud Vision (10 minutes)")
        
        print(f"{YELLOW}🔗 Open this link:{RESET}")
        print(f"   https://console.cloud.google.com\n")
        
        print(f"📋 {YELLOW}Quick Setup:{RESET}")
        print(f"   1. Create new project: 'yourefuture-mvp'")
        print(f"   2. Search for 'Vision API' and enable it")
        print(f"   3. Go to Service Accounts: https://console.cloud.google.com/iam-admin/serviceaccounts")
        print(f"   4. Create service account: 'yourefuture-vision'")
        print(f"   5. Add role: Editor")
        print(f"   6. Create JSON key and download")
        print(f"   7. Save the file as: google-cloud-key.json\n")
        
        key_path = input(f"{BLUE}Path to google-cloud-key.json: {RESET}").strip()
        
        if not key_path or not os.path.exists(key_path):
            print(f"{RED}❌ File not found!{RESET}")
            return False
        
        try:
            with open(key_path) as f:
                creds = json.load(f)
            
            self.env_vars['GOOGLE_CLOUD_PROJECT_ID'] = creds['project_id']
            self.env_vars['GOOGLE_CLOUD_API_KEY_PATH'] = os.path.abspath(key_path)
            
            # Copy key file to secure location
            dest_path = self.workspace_root / '.secrets' / 'google-cloud-key.json'
            dest_path.parent.mkdir(exist_ok=True)
            with open(dest_path, 'w') as f:
                json.dump(creds, f)
            
            self.env_vars['GOOGLE_CLOUD_API_KEY_PATH'] = str(dest_path)
            
            print(f"{GREEN}✅ Google Cloud credentials saved!{RESET}")
            return True
        except Exception as e:
            print(f"{RED}❌ Error reading file: {e}{RESET}")
            return False
    
