# Color codes
GREEN = '\033[92m'
RED = '\033[91m'
YELLOW = '\033[93m'
BLUE = '\033[94m'
RESET = '\033[0m'

class APIRegistrationHelper:
    def __init__(self):
        self.workspace_root = Path(__file__).parent.parent
        self.env_file = self.workspace_root / ".env.local"
        self.env_vars = self.load_env()
        self.email = "raut.deepakdesign@gmail.com"
        
    def load_env(self) -> Dict[str, str]:
        """Load existing .env.local or create empty dict"""
        if self.env_file.exists():
            env = {}
            with open(self.env_file) as f:
                for line in f:
                    line = line.strip()
                    if line and not line.startswith('#'):
                        key, _, val = line.partition('=')
                        env[key] = val
            return env
        return {}
    
    def save_env(self):
        """Save env vars to .env.local"""
        with open(self.env_file, 'w') as f:
            f.write(f"# Generated by register-apis.py\n")
            f.write(f"# Email: {self.email}\n")
            f.write(f"# Last updated: {time.strftime('%Y-%m-%d %H:%M:%S')}\n\n")
            
            # Group by category
            sections = {
                'API Keys': {},
                'Feature Flags': {},
                'Rate Limiting': {},
            }
            
            for key, val in sorted(self.env_vars.items()):
                if key.endswith('_KEY') or key.endswith('_TOKEN') or key.endswith('_ID'):
                    sections['API Keys'][key] = val
                elif key.startswith('FEATURE_'):
                    sections['Feature Flags'][key] = val
                elif 'RATE' in key or 'QUOTA' in key or 'LIMIT' in key:
                    sections['Rate Limiting'][key] = val
            
            for section, items in sections.items():
                if items:
                    f.write(f"\n# ========== {section} ==========\n")
                    for key, val in sorted(items.items()):
                        f.write(f"{key}={val}\n")
    
    def print_header(self, text: str):
        """Print section header"""
        print(f"\n{BLUE}{'='*60}{RESET}")
        print(f"{BLUE}{text:^60}{RESET}")
        print(f"{BLUE}{'='*60}{RESET}\n")
    
    def print_status(self, api: str, status: str, color: str):
        """Print API status"""
        print(f"{color}  {api:<30} {status}{RESET}")
    
    def verify_huggingface(self, token: str) -> bool:
        """Verify Hugging Face token"""
        try:
            resp = requests.get(
                'https://huggingface.co/api/whoami',
                headers={'Authorization': f'Bearer {token}'},
                timeout=5
            )
            return resp.status_code == 200
        except:
            return False
    
